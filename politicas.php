<p style="font-weight: bold;">Política de Garantías de Servicios y Soluciones Técnicas</p>

<p><strong>Definiciones:</strong></p>

<p>1. <strong>Garantía:</strong> Obligación temporal a cargo del proveedor, de responder porque el servicio prestado cumpla las condiciones de idoneidad, calidad y seguridad legalmente exigibles o las ofrecidas.</p>

<p>2. <strong>Consumidor:</strong> Toda persona natural o jurídica que, como destinatario final, adquiera, disfrute o utilice un determinado servicio de la Compañía, independientemente de su naturaleza, para la satisfacción de una necesidad propia, privada, familiar o doméstica y empresarial cuando no esté ligada intrínsecamente a su actividad económica.</p>

<p>3. <strong>Canales de Servicio Autorizado:</strong> Corresponden a los únicos canales autorizados por la Compañía para hacer la recepción, verificación y registro de solicitudes de efectividad de la garantía.</p>

<p><strong>Garantía:</strong></p>

<p>La Compañía garantizará que los servicios adquiridos a través de los diferentes canales de comercialización son aptos para el uso indicado y cumplen con las condiciones de idoneidad, calidad y seguridad legalmente exigibles o las ofrecidas.</p>

<p>La garantía no supone un derecho automático a solicitar servicios por simple insatisfacción. En cualquier caso, deben demostrarse circunstancias objetivas que denoten la falta de idoneidad, calidad o seguridad del servicio para que el Consumidor pueda realizar reclamaciones por efectividad de la garantía.</p>

<p><strong>Aspectos incluidos en la garantía:</strong></p>

<p>De conformidad con la Ley 1480 de 2011 (“Estatuto del Consumidor”), la garantía legal comprende:</p>

<ul>
    <li>El derecho a obtener la reparación gratuita de los defectos del servicio adquirido y el suministro oportuno. Si el servicio adquirido no admite reparación, se tendrá derecho a una reposición por el mismo servicio o de similares características o a la devolución del dinero pagado por el mismo.</li>
    <li>El derecho a contar con asistencia técnica telefónica para el uso y mantenimiento de los servicios prestados y para su utilización, de acuerdo con la naturaleza de estos. La asistencia técnica podrá tener un costo adicional al precio del servicio.</li>
    <li>El derecho a contar con la mano de obra capacitada, por el término de Garantía que la Compañía anuncie en sus servicios como en la presente política.</li>
</ul>

<p><strong>Exclusiones y Pérdida de la Garantía:</strong></p>

<p>El consumidor perderá el derecho a reclamar por la garantía legal en los siguientes eventos:</p>

<ul>
    <li>Cuando en el producto se evidencie un inadecuado uso, conservación, cuidado o manipulación por parte del Consumidor.</li>
    <li>Cuando se identifique que el servicio ha sido reparado o intervenido por un tercero diferente a nuestros técnicos autorizados, o contenga señas de manipulación directa por parte del consumidor, piezas no originales, o cualquier otra circunstancia que denote que su estructura o componentes son diferentes de aquellos bajo los cuales la Compañía lo puso a disposición del consumidor.</li>
    <li>Cuando el producto presente golpes, abolladuras, o alteraciones por caídas o incidentes similares.</li>
    <li>Cuando el consumidor desatienda las instrucciones de instalación, uso o mantenimiento indicadas.</li>
    <li>Cuando se trate de piezas del producto que resulten consumibles, es decir, piezas que se deterioren de forma normal y corriente debido al uso regular del producto.</li>
    <li>Cuando se presenten situaciones de caso fortuito o fuerza mayor, tales como daños generados por fenómenos naturales como terremotos, inundaciones, tormentas eléctricas, etc.</li>
    <li>Cuando se presenten condiciones accidentales o provocadas por circunstancias ajenas al control de la Compañía como humedad, incendios, alteraciones en corrientes o voltajes eléctricos, cambios súbitos de presión, vandalismo, robo o circunstancias similares imputables a terceros diferentes a la Compañía.</li>
    <li>Cuando la razón para solicitar la garantía sea el desgaste natural del producto por su uso continuado por tiempos prolongados.</li>
</ul>

<p><strong>Término de la Garantía:</strong></p>

<p>Dependiendo de la categoría del servicio adquirido, este tendrá unos términos y condiciones de garantía distintos. El término de la garantía del servicio podrá ser consultado en la orden de servicio suscrita.</p>

<p>Los repuestos usados por la compañía en el servicio técnico tendrán la siguiente garantía:</p>

<ul>
    <li>Consumibles: Los repuestos consumibles son todos aquellos elementos funcionales que están sometidos a desgaste por el uso del producto y que pueden ser reemplazados sin el uso de herramientas. Estos repuestos cuentan con 90 días de garantía a partir de su fecha de instalación.</li>
    <li>Electromecánicos: Los repuestos electromecánicos son aquellos componentes funcionales que si requieren un desmontaje completo del producto para ser reemplazados y el uso de herramienta especializada para la verificación de su funcionamiento. Esta clasificación de repuestos cuenta con un tiempo de garantía de 3 a 6 meses a partir de su fecha de su instalación dependiendo del trabajo realizado.</li>
</ul>

<p><strong>Proceso para la efectividad de la garantía:</strong></p>

<p>A efectos de que se pueda hacer efectiva la garantía sobre los servicios ofrecidos por la Compañía, el consumidor deberá cumplir con el proceso que a continuación se describe:</p>

<ol>
    <li>Si el servicio adquirido presenta fallas de calidad, idoneidad, funcionalidad o seguridad, el consumidor deberá notificar por los canales de atención.</li>
    <li>El producto objeto del servicio prestado debe entregarse completo y limpio. Los asesores de servicio al cliente documentarán la solicitud de efectividad de la garantía a través de un aplicativo de la Compañía, donde quedarán consignados algunos de los datos del consumidor, el lugar y fecha de servicio, referencia del producto, tipo de falla, entre otros.</li>
    <li>Una vez recibido el producto conforme a las especificaciones antes mencionadas, la Compañía contará con 15 días hábiles para dar una respuesta sobre la solicitud de efectividad de la garantía.</li>
</ol>

<p><strong>Constancia de recibo y reparación:</strong></p>

<p>Cuando el consumidor entregue un producto para hacer efectiva la garantía, la Compañía expedirá una constancia de recibo física o virtual relacionando los motivos de la reclamación, la piezas sustituidas o reparadas, la fecha en que fue realizada la reparación y la fecha en que se devolvió el producto reparado.</p>

<p><strong>Disponibilidad de repuestos:</strong></p>

<p>La Compañía garantiza el suministro de repuestos, unidades de reposición, insumos y mano de obra capacitada para el adecuado funcionamiento de los productos que se atiendan para servicio técnico, durante todo el tiempo de duración de la garantía correspondiente a cada producto.</p>






